// this function is being called from the app.js and it has the page name
export function modelPageName(pgName) {
    // console.log(pgName);
    // $("#app").html(eval(pgName));
    if(pgName == "homeContent") {
        $("nav a").removeClass("black-text").addClass("white-text");
        $(".logo").removeClass("logo-white").addClass("logo-black");
        //or just repeat the line and change nav a to span
    } else {
        $("nav a").removeClass("white-text").addClass("black-text");
        $(".logo").removeClass("logo-black").addClass("logo-white");
    }

    try {
      eval(pgName);
      $("#app").html(eval(pgName));
    } catch (e) {
      console.log("hello", e);
    }
}